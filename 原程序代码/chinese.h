/********************************************************************************/
//函数名:tftShowGB
//功  能:显示GB2312汉字及ASCII字串
//参  数:str为要显示的字串
//返回值:
//备  注:外部  GB2312码以大端模式存放
//更  新:2012.3.2									
/********************************************************************************/
void tftShowGB(unsigned char *str)
{
	unsigned char hzTab[32],i;	//存放汉字字模
	while(*str)
	{
		if(*str&0x80)
		{//是汉字
			if(hzk16_gbZiMo(str[0],str[1],hzTab))//获得字模	 str大端模式
			{	//无法获得字模
				for(i=0;i<32;i++)
					hzTab[i]=CHINESE_NUL_CHAR[i];
			}
			tftShowChar(16,16,hzTab);//打字模
			str+=2;//一个汉字两个字节
		}else{//是ASCII码
			if(*str=='\n')
				tftSetCharXY(0,TFT_CHAR_Y+16);
			else
				tftShowChar(8,16,CHINESE_CHAR8X16[*str-32]);//打字模
			str++;
		}
	}
}




/********************************************************************************/
//函数名:tftShowUNI
//功  能:显示UNICODE汉字及字母
//参  数:str为要显示的字串,最后两个字节必须为0
//返回值:
//备  注:外部	,str指向的必须是正确的UNICODE码(每个UNICODE码两个字节)
//			 UNICODE码以小端模式存放
//更  新:2012.3.2									
/********************************************************************************/
void tftShowUNI(unsigned char *str)
{
	unsigned char hzTab[32],i;	//存放汉字字模 若内存小可采用像tmp_buf的方法
	while(str[0]||str[1])//两位不都为0时 是UNICODE码 (两位都为0时 字串结束)
	{
		if(str[1])//高位为0 则是兼容的ASCII码 
		{
			//是汉字
			if(hzk16_uniZiMo(str[1],str[0],hzTab))//获得字模 str小端模式
			{	//无法获得字模
				for(i=0;i<32;i++)
					hzTab[i]=CHINESE_NUL_CHAR[i];
			}
			tftShowChar(16,16,hzTab);//打字模
		}else{
			//是UNICODE字母
			tftShowChar(8,16,CHINESE_CHAR8X16[str[0]-32]);//打字模
		}
		str+=2;//一个字两个字节
	}
}


